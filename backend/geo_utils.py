def extract_town(authority: str, subject: str = "") -> str:
    """Extract town name from Bulgarian contracting authority."""
    if not authority or pd.isna(authority):
        return "неизвестен"
    
    text = str(authority).lower()
    
    # Pattern: Община X / Община град X
    import re
    m = re.search(r'община\s+(?:град\s+)?(.+?)(?:,|$)', text)
    if m:
        return m.group(1).strip()
    
    # Pattern: гр. X
    m = re.search(r'гр\.\s*(.+?)(?:,|\)|$)', text)
    if m:
        return m.group(1).strip()
    
    # Pattern: X - София (location suffix)
    m = re.search(r'["\']?([^"\']+?)["\']?\s*[-–]\s*(софия|варна|бургас|пловдив|русе|плевен|добрич)', text)
    if m:
        return m.group(2).strip()
    
    # Named cities in the text
    for town in BG_TOWNS:
        if town in text:
            return town
    
    return "неизвестен"

def geocode_town(town: str) -> tuple:
    """Get lat/lng for a Bulgarian town."""
    town_lower = town.lower().strip()
    if town_lower in BG_TOWNS:
        return BG_TOWNS[town_lower]
    return (42.6977, 25.3219)  # default: center of Bulgaria

